class SNEP_CRM_Extreme:
    def __init__(self):
        self.name = "SNEP CRM EXTREME"
        self.version = "3.0"
        self.clients_df = pd.DataFrame()
        self.transactions_df = pd.DataFrame()
        self.produits_df = pd.DataFrame()
        self.init_produits()
        print(f"\n╔{'═'*50}╗")
        print(f"║  ⚡ {self.name} - Version {self.version} ⚡".ljust(51) + "║")
        print(f"╚{'═'*50}╝\n")

    def init_produits(self):
        produits = [
            ['CHLORE GAZEUX 99.5%', 'Chlore', 'TONNES', 1500],
            ['CHLORE LIQUIDE 99.8%', 'Chlore', 'TONNES', 1800],
            ['SOUDE CAUSTIQUE LIQ 30%', 'Soude', 'TONNES', 800],
            ['SOUDE CAUSTIQUE LIQ 48%', 'Soude', 'TONNES', 950],
            ['ACIDE CHLORHYDRIQUE 31%', 'Acide', 'TONNES', 600],
            ['EAU DE JAVEL 12°', 'Javel', 'LITRES', 2.5],
            ['PVC SUSPENSION S60', 'PVC', 'TONNES', 1200],
        ]
        self.produits_df = pd.DataFrame(produits, columns=['Nom', 'Catégorie', 'Unité', 'Prix'])
        print(f"📦 {len(self.produits_df)} produits chargés")

    def ajouter_client(self, societe, secteur='', email='', telephone='', ville='Casablanca'):
        id_client = len(self.clients_df) + 1
        nouveau = pd.DataFrame([{
            'ID': id_client, 'Société': societe, 'Secteur': secteur,
            'Email': email, 'Téléphone': telephone, 'Ville': ville,
            'Date': datetime.now().strftime('%Y-%m-%d')
        }])
        self.clients_df = pd.concat([self.clients_df, nouveau], ignore_index=True)
        return id_client

    def ajouter_transaction(self, client_id, produit, quantite, prix, paiement='COMPTANT'):
        id_trans = len(self.transactions_df) + 1
        ca = quantite * prix
        alerte = '✓ SUIVI'
        if 'CHÈQUE' in paiement.upper() or 'CHEQUE' in paiement.upper():
            alerte = '🟡 À SURVEILLER'

        nouvelle = pd.DataFrame([{
            'ID': id_trans, 'Date': datetime.now().strftime('%Y-%m-%d'),
            'Client_ID': client_id, 'Produit': produit, 'Quantité': quantite,
            'Prix': prix, 'CA': ca, 'Paiement': paiement, 'Alerte': alerte
        }])
        self.transactions_df = pd.concat([self.transactions_df, nouvelle], ignore_index=True)
        return id_trans

    def generer_donnees_test(self):
        clients = ['ONEE', 'LYDEC', 'OCP', 'COSUMAR', 'LESIEUR']
        for c in clients:
            self.ajouter_client(c)

        for _ in range(15):
            client = np.random.randint(1, 6)
            produit = np.random.choice(self.produits_df['Nom'].tolist())
            prix = self.produits_df[self.produits_df['Nom'] == produit]['Prix'].values[0]
            qte = np.random.randint(10, 100)
            paiement = np.random.choice(['COMPTANT', 'CHÈQUE 30j'])
            self.ajouter_transaction(client, produit, qte, prix, paiement)

        print("✅ Données de test générées")

