class CRMInterface:
    def __init__(self, crm):
        self.crm = crm
        self.create_interface()

    def create_interface(self):
        display(HTML("""
        <style>
        .tab { margin: 10px; }
        button { margin: 5px; padding: 10px; }
        </style>
        """))

        self.tab = widgets.Tab()
        self.tab.children = [
            self.create_dashboard(),
            self.create_clients(),
            self.create_transactions(),
            self.create_alertes()
        ]
        self.tab.set_title(0, '📊 Tableau de bord')
        self.tab.set_title(1, '👥 Clients')
        self.tab.set_title(2, '💰 Transactions')
        self.tab.set_title(3, '⚠️ Alertes')
        display(self.tab)

    def create_dashboard(self):
        container = widgets.VBox()
        ca_total = self.crm.transactions_df['CA'].sum() if len(self.crm.transactions_df) > 0 else 0
        nb_trans = len(self.crm.transactions_df)
        nb_clients = len(self.crm.clients_df)

        kpi_html = f"""
        <div style='display:flex; justify-content:space-around; margin:20px;'>
            <div style='background:#667eea; color:white; padding:20px; border-radius:10px; width:200px; text-align:center;'>
                <h2>💰 CA Total</h2>
                <h1>{ca_total:,.0f} DHS</h1>
            </div>
            <div style='background:#764ba2; color:white; padding:20px; border-radius:10px; width:200px; text-align:center;'>
                <h2>📊 Transactions</h2>
                <h1>{nb_trans}</h1>
            </div>
            <div style='background:#f39c12; color:white; padding:20px; border-radius:10px; width:200px; text-align:center;'>
                <h2>👥 Clients</h2>
                <h1>{nb_clients}</h1>
            </div>
        </div>
        """
        container.children = [widgets.HTML(kpi_html)]

        if len(self.crm.transactions_df) > 0:
            html = "<h3>📋 Dernières transactions</h3>"
            html += self.crm.transactions_df.tail(5).to_html(index=False)
            container.children += (widgets.HTML(html),)

        return container

    def create_clients(self):
        container = widgets.VBox()
        btn_add = widgets.Button(description='➕ Nouveau Client', button_style='success')

        def on_add_click(b):
            self.show_add_client_form()
        btn_add.on_click(on_add_click)

        container.children = [btn_add]

        if len(self.crm.clients_df) > 0:
            html = "<h3>Liste des clients</h3>"
            html += self.crm.clients_df.to_html(index=False)
            container.children += (widgets.HTML(html),)

        return container

    def show_add_client_form(self):
        form = widgets.VBox()
        societe = widgets.Text(description='Société:')
        secteur = widgets.Text(description='Secteur:')
        email = widgets.Text(description='Email:')
        telephone = widgets.Text(description='Téléphone:')
        ville = widgets.Text(description='Ville:', value='Casablanca')

        def save(b):
            self.crm.ajouter_client(societe.value, secteur.value, email.value, telephone.value, ville.value)
            clear_output(wait=True)
            self.create_interface()

        btn_save = widgets.Button(description='💾 Enregistrer', button_style='success')
        btn_save.on_click(save)
        form.children = [societe, secteur, email, telephone, ville, btn_save]
        display(form)

    def create_transactions(self):
        container = widgets.VBox()

        if len(self.crm.clients_df) > 0 and len(self.crm.produits_df) > 0:
            clients = [f"{row['ID']} - {row['Société']}" for _, row in self.crm.clients_df.iterrows()]
            client_select = widgets.Dropdown(options=clients, description='Client:')

            produits = self.crm.produits_df['Nom'].tolist()
            produit_select = widgets.Dropdown(options=produits, description='Produit:')

            quantite = widgets.FloatText(value=1, description='Quantité:')
            prix = widgets.FloatText(description='Prix:')
            paiement = widgets.Dropdown(options=['COMPTANT', 'CHÈQUE 30j', 'VIREMENT'], description='Paiement:')

            def update_price(change):
                if change['new']:
                    p = self.crm.produits_df[self.crm.produits_df['Nom'] == change['new']]['Prix'].values[0]
                    prix.value = p
            produit_select.observe(update_price, names='value')

            def save_transaction(b):
                client_id = int(client_select.value.split(' - ')[0])
                self.crm.ajouter_transaction(client_id, produit_select.value, quantite.value, prix.value, paiement.value)
                clear_output(wait=True)
                self.create_interface()

            btn_save = widgets.Button(description='📥 Enregistrer', button_style='success')
            btn_save.on_click(save_transaction)

            container.children = [
                widgets.HTML("<h3>➕ Nouvelle transaction</h3>"),
                client_select, produit_select, quantite, prix, paiement, btn_save
            ]

        if len(self.crm.transactions_df) > 0:
            html = "<h3>📋 Historique</h3>"
            html += self.crm.transactions_df.to_html(index=False)
            container.children += (widgets.HTML(html),)

        return container

    def create_alertes(self):
        container = widgets.VBox()

        if len(self.crm.transactions_df) > 0:
            cheques = self.crm.transactions_df[
                self.crm.transactions_df['Paiement'].str.contains('CHÈQUE|CHEQUE', na=False)
            ]
            if len(cheques) > 0:
                html = "<h3 style='color:orange;'>🟡 Chèques en cours</h3>"
                html += cheques.to_html(index=False)
                container.children += (widgets.HTML(html),)

        return container

