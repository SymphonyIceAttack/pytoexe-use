    def load_progress(self):
        if os.path.exists(SAVE_FILE):
            try:
                with open(SAVE_FILE, "r", encoding="utf-8") as f:
                    data = json.load(f)
                    self.level = data.get("level", 1)
                    self.exp = data.get("exp", 0)
                    self.correct_diagnoses = data.get("correct_diagnoses", 0)
                    self.total_patients = data.get("total_patients", 0)
            except:
                self.level = 1
                self.exp = 0
                self.correct_diagnoses = 0
                self.total_patients = 0
        else:
            self.level = 1
            self.exp = 0
            self.correct_diagnoses = 0
            self.total_patients = 0
    
    def save_progress(self):
        data = {
            "level": self.level,
            "exp": self.exp,
            "correct_diagnoses": self.correct_diagnoses,
            "total_patients": self.total_patients
        }
        with open(SAVE_FILE, "w", encoding="utf-8") as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
    
    def calculate_exp_needed(self):
        return 300 + (self.level - 1) * 150
    
    def add_exp(self, amount):
        self.exp += amount
        needed = self.calculate_exp_needed()
        while self.exp >= needed and self.level < 10:
            self.exp -= needed
            self.level += 1
            messagebox.showinfo("Повышение уровня", f"🌟 Поздравляем! Вы достигли {self.level} уровня!")
            needed = self.calculate_exp_needed()
        self.update_stats()
        self.save_progress()
    
    def update_stats(self):
        needed = self.calculate_exp_needed()
        accuracy = (self.correct_diagnoses / self.total_patients * 100) if self.total_patients > 0 else 0
        text = f"👨‍⚕️ Доктор - Уровень {self.level} | Опыт: {self.exp}/{needed} | 🏆 Вылечено: {self.correct_diagnoses}/{self.total_patients} | 📊 Точность: {accuracy:.1f}%"
        if hasattr(self, 'doctor_label'):
            self.doctor_label.config(text=text)
    
    def get_random_patient(self):
        available = {name: data for name, data in DISEASES_DB.items() if data["required_level"] <= self.level}
        if not available:
            return None
        
        disease_name = random.choice(list(available.keys()))
        disease_data = available[disease_name]
        
        gender = random.choice(["female", "male"])
        if gender == "female":
            first_name = random.choice(FEMALE_NAMES)
            last_name = random.choice(FEMALE_LAST_NAMES)
            gender_display = "♀ Женщина"
        else:
            first_name = random.choice(MALE_NAMES)
            last_name = random.choice(MALE_LAST_NAMES)
            gender_display = "♂ Мужчина"
        age = random.randint(18, 85)
        
        # Генерация симптомов: 3-4 истинных симптома + 1-2 ложных
        true_symptoms_list = random.sample(disease_data["true_symptoms"], min(4, len(disease_data["true_symptoms"])))
        num_fake = random.randint(1, 2)
        fake_symptoms_list = random.sample(FAKE_SYMPTOMS, num_fake)
        
        all_symptoms = true_symptoms_list + fake_symptoms_list
        random.shuffle(all_symptoms)
        symptoms = ", ".join(all_symptoms)
        
        labs = {}
        for lab_name, lab_info in ALL_LABS.items():
            if lab_name in disease_data.get("affected_labs", {}):
                direction = disease_data["affected_labs"][lab_name]["direction"]
                if direction == "high" and isinstance(lab_info["normal"], tuple):
                    value = lab_info["normal"][1] * random.uniform(1.2, 2.5)
                    if isinstance(lab_info["normal"][1], int):
                        value = int(value)
                    else:
                        value = round(value, 1)
                elif direction == "low" and isinstance(lab_info["normal"], tuple):
                    value = lab_info["normal"][0] * random.uniform(0.2, 0.8)
                    if isinstance(lab_info["normal"][0], int):
                        value = int(value)
                    else:
                        value = round(value, 1)
                else:
                    value = "патология"
            else:
                if isinstance(lab_info["normal"], tuple):
                    value = random.uniform(lab_info["normal"][0], lab_info["normal"][1])
                    if isinstance(lab_info["normal"][0], int):
                        value = int(value)
                    else:
                        value = round(value, 1)
                else:
                    value = lab_info["normal"]
            labs[lab_name] = value
        
        return {
            "name": f"{first_name} {last_name}",
            "age": age,
            "gender": gender_display,
            "symptoms": symptoms,
            "disease_name": disease_name,
            "disease_data": disease_data,
            "labs": labs,
            "treatments": disease_data.get("treatments", []),
            "correct_medicines": disease_data["correct_medicines"]
        }
    
