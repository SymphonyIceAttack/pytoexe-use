    def order_lab(self):
        if not self.current_patient:
            messagebox.showwarning("Предупреждение", "Сначала примите пациента! Нажмите 'ПРИНЯТЬ ПАЦИЕНТА'")
            return
        
        selection = self.available_listbox.curselection()
        if not selection:
            messagebox.showwarning("Предупреждение", "Выберите анализ из списка!")
            return
        
        lab_name = self.available_listbox.get(selection[0]).split(" (")[0]
        
        if lab_name in self.ordered_labs:
            messagebox.showwarning("Предупреждение", "Этот анализ уже заказан!")
            return
        
        self.attempts_used += 1
        self.ordered_labs.append(lab_name)
        
        value = self.current_patient["labs"][lab_name]
        unit = ALL_LABS[lab_name].get("unit", "")
        unit_str = f" {unit}" if unit else ""
        
        self.labs_text.insert(tk.END, f"• {lab_name}: {value}{unit_str}\n")
        self.labs_text.see(tk.END)
        
        self.attempts_label.config(text=f"🔬 Осталось анализов: {self.max_attempts - self.attempts_used} из {self.max_attempts}")
        
        self.available_listbox.delete(selection[0])
        
        if self.attempts_used >= self.max_attempts:
            self.patient_dies()
    
    def prescribe_medicine(self):
        if not self.current_patient:
            messagebox.showwarning("Предупреждение", "Сначала примите пациента! Нажмите 'ПРИНЯТЬ ПАЦИЕНТА'")
            return
        
        selection = self.medicines_listbox.curselection()
        if not selection:
            messagebox.showwarning("Предупреждение", "Выберите лекарство из списка!")
            return
        
        medicine = self.medicines_listbox.get(selection[0])
        self.check_medicine(medicine)
    
    def check_medicine(self, medicine):
        correct_meds = [m.lower() for m in self.current_patient["correct_medicines"]]
        
        if medicine.lower() in correct_meds:
            exp_gain = self.current_patient["disease_data"]["exp_reward"] * (1 + (self.level - 1) * 0.1)
            exp_gain = int(exp_gain)
            self.total_patients += 1
            self.correct_diagnoses += 1
            self.add_exp(exp_gain)
            
            msg = f"🎉 ПРАВИЛЬНО! Лекарство '{medicine}' помогло пациенту!\n\n"
            msg += f"🏥 Диагноз: {self.current_patient['disease_name']}\n"
            if self.current_patient["treatments"]:
                msg += f"💊 Рекомендуемое лечение: {', '.join(self.current_patient['treatments'][:2])}\n"
            msg += f"✨ Получено опыта: +{exp_gain}"
            
            messagebox.showinfo("Победа!", msg)
            self.patient_status_label.config(text="⚠️ Нет активного пациента", fg='orange')
            self.patient_name_label.config(text="")
            self.patient_symptoms_label.config(text="")
            self.attempts_label.config(text="")
            self.labs_text.delete(1.0, tk.END)
            self.current_patient = None
        else:
            penalty = 3
            self.attempts_used += penalty
            
            self.labs_text.insert(tk.END, f"\n⚠️ Лекарство '{medicine}' не подошло! -{penalty} анализов\n")
            self.labs_text.see(tk.END)
            
            self.attempts_label.config(text=f"🔬 Осталось анализов: {self.max_attempts - self.attempts_used} из {self.max_attempts}")
            
            if self.attempts_used >= self.max_attempts:
                self.patient_dies()
    
