import random

answers = {
    "привет": ["Привет!", "Здравствуй!", "Приветствую!", "Салют!"],
    "как дела": ["Хорошо, а у тебя?", "Отлично!", "Нормально, спасибо."],
    "хорошо": ["Рад за тебя!", "Отлично!", "Классно!"],
    "плохо": ["Мне жаль.", "Сочувствую.", "Всё наладится!"],
    "что делаешь": ["Общаюсь с тобой!", "Думаю о жизни.", "Скучаю немного."],
    "имя": ["Меня зовут Робот.", "Я простой собеседник.", "Зови меня Бот."],
    "зовут": ["Меня зовут Робот.", "Я простой собеседник.", "Зови меня Бот."],
    "пока": ["Пока!", "До свидания!", "Ещё увидимся!"]
}

dont = ["Интересно...", "Расскажи ещё.", "Я не понял.", "Правда?"]

print("🤖 Робот-собеседник")

while True:
    human = input("🧑 Вы: ")
    human = human.lower()

    if human == "пока":
        print("🤖:", random.choice(answers["пока"]))
        break

    gotit = False
    for goida in answers:
        if goida in human:
            print("🤖:", random.choice(answers[goida]))
            gotit = True
            break

    if not gotit:
        print("🤖:", random.choice(dont))