max_floor = 729
current_bpm = 180
times = [0.0] * (max_floor + 1)  # 索引从1开始
time_accum = 0.0
speed_dict = {f: bpm for f, bpm in set_speeds}

for floor in range(1, max_floor + 1):
    delta = 30.0 / current_bpm
    time_accum += delta
    times[floor] = time_accum
    if floor in speed_dict:
        current_bpm = speed_dict[floor]

# 输出结果，用斜杠分隔
result = '/'.join(f"{t:.6f}" for t in times[1:])
print(result)