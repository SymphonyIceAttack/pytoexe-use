import time
import sys


def slow_print(text, delay=0.03):

    for char in text:
        sys.stdout.write(char)
        sys.stdout.flush()
        time.sleep(delay)
    print()


def get_choice(prompt, valid_options):

    while True:
        choice = input(prompt).strip()
        if choice in valid_options:
            return choice
        print("ЦУП: Ошибка ввода. Пожалуйста, выберите один из предложенных вариантов.")


def game_over(reason):

    print("\n" + "=" * 50)
    slow_print(f"МИССИЯ ПРОВАЛЕНА: {reason}")
    slow_print("Вы лишены допуска к полетам. История пошла по другому пути.")
    print("=" * 50 + "\n")
    sys.exit()


