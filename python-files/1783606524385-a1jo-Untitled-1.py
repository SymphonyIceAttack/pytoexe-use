# -*- coding: utf-8 -*-

def پیدا_کن_قافیه(کلمه_مبدا, تعداد_حروف_قافیه):
    """
    این تابع کلماتی را پیدا می‌کند که با کلمه مبدا هم‌قافیه هستند.
    """
    # یک فرهنگ لغت کوچک از کلمات فارسی (می‌توانید خودتان آن را بزرگ‌تر کنید)
    فرهنگ_لغت = [
        "آب", "باب", "شب", "مهتاب", "روز", "نو", "خوب", "دوست", 
        "گل", "بلبل", "دل", "همدل", "کار", "یادگار", "شکار",
        "خورشید", "امید", "نوید", "سپید"
    ]
    
    # بخش آخر کلمه مبدا را به تعداد حروف مورد نظر جدا می‌کنیم
    بخش_قافیه_مبدا = کلمه_مبدا[-تعداد_حروف_قافیه:]
    
    کلمات_هم_قافیه = []
    for کلمه in فرهنگ_لغت:
        # اگر کلمه با کلمه مبدا یکی نباشد و بخش آخر آن با بخش قافیه مبدا یکی باشد
        if کلمه != کلمه_مبدا and کلمه.endswith(بخش_قافیه_مبدا):
            کلمات_هم_قافیه.append(کلمه)
            
    return کلمات_هم_قافیه

# -------- بخش اصلی برنامه (محیط کاربری) --------
print("="*40)
print("    به نرم‌افزار قافیه‌یاب خوش آمدید!")
print("="*40)

while True:
    # گرفتن ورودی از کاربر
    کلمه_مبدا = input("\nلطفاً کلمه مورد نظر خود را وارد کنید (یا 'exit' برای خروج): ")
    
    # شرط خروج از برنامه
    if کلمه_مبدا.lower() == 'exit':
        print("خداحافظ!")
        break
    
    # بررسی اینکه کاربر کلمه را درست وارد کرده باشد
    if not کلمه_مبدا.isalpha():
        print("خطا: لطفاً فقط از حروف فارسی استفاده کنید.")
        continue
        
    try:
        تعداد_حروف = int(input("چند حرف از آخر کلمه برای قافیه در نظر گرفته شود؟ (مثلاً 2): "))
        if تعداد_حروف <= 0 or تعداد_حروف > len(کلمه_مبدا):
            print(f"خطا: تعداد حروف باید بین 1 و {len(کلمه_مبدا)} باشد.")
            continue
    except ValueError:
        print("خطا: لطفاً یک عدد صحیح وارد کنید.")
        continue

    # پیدا کردن قافیه‌ها
    نتایج = پیدا_کن_قافیه(کلمه_مبدا, تعداد_حروف)
    
    # نمایش نتیجه
    if نتایج:
        print(f"\nکلماتی که با '{کلمه_مبدا}' هم‌قافیه هستند:")
        for کلمه in نتایج:
            print(f"- {کلمه}")
    else:
        print(f"\nهیچ کلمه‌ای با '{کلمه_مبدا}' و {تعداد_حروف} حرف قافیه پیدا نشد.")
    
    print("-"*40)