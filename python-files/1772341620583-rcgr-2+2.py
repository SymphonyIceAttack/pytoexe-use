# Color codes
class Colors:
    RED = '\033[91m'
    GREEN = '\033[92m'
    YELLOW = '\033[93m'
    BLUE = '\033[94m'
    MAGENTA = '\033[95m'
    CYAN = '\033[96m'
    WHITE = '\033[97m'
    RESET = '\033[0m'
    BOLD = '\033[1m'

def clear_screen():
    """Clear the terminal screen"""
    os.system('clear' if os.name != 'nt' else 'cls')

def animate_logo(duration=10, fps=10):
    """
    Animate the Mr. Robot logo with movement and color cycling

    Args:
        duration: How long to run the animation (seconds)
        fps: Frames per second
    """
    frames = cycle(MR_ROBOT_LOGO)
    colors = cycle([Colors.RED, Colors.GREEN, Colors.CYAN, Colors.MAGENTA])

    frame_delay = 1.0 / fps
    total_frames = int(duration * fps)

    try:
        for i in range(total_frames):
            clear_screen()

            current_frame = next(frames)
            current_color = next(colors)

            # Print colored frame
            print(f"{current_color}{current_frame}{Colors.RESET}")

            # Add subtitle
            if i % 20 < 10:
                subtitle = "[ HELLO, FRIEND ]"
            else:
                subtitle = "[ FSOCIETY ]"

            print(f"\n{Colors.BOLD}{Colors.WHITE}{subtitle.center(80)}{Colors.RESET}")

            # Progress indicator
            progress = "â–ˆ" * (i % 40)
            print(f"\n{Colors.CYAN}{progress}{Colors.RESET}")

            time.sleep(frame_delay)

    except KeyboardInterrupt:
        clear_screen()
        print(f"{Colors.RED}[!] Animation interrupted{Colors.RESET}")
        sys.exit(0)

def main():
    """Main function to run the Mr. Robot logo animation"""
    clear_screen()

    # Welcome message
    print(f"{Colors.CYAN}{Colors.BOLD}")
    print("=" * 80)
    print("MR. ROBOT ANIMATED LOGO TOOL".center(80))
    print("=" * 80)
    print(f"{Colors.RESET}\n")

    print(f"{Colors.WHITE}Starting animation... Press Ctrl+C to stop{Colors.RESET}\n")
    time.sleep(1)

    # Run animation
    animate_logo(duration=30, fps=8)

    # Cleanup
    clear_screen()
    print(f"\n{Colors.GREEN}[âœ“] Animation complete. Stay safe, friend.{Colors.RESET}\n")

if __name__ == "__main__":
    main()
    