def save_session(login, mac_hash):
    with open(SESSION_FILE, 'w', encoding='utf-8') as f:
        f.write(f"{login}|{mac_hash}|{datetime.datetime.now().isoformat()}")

def load_session():
    if not os.path.exists(SESSION_FILE):
        return None, None
    with open(SESSION_FILE, 'r', encoding='utf-8') as f:
        content = f.read().strip()
        if '|' in content:
            parts = content.split('|')
            if len(parts) >= 2:
                return parts[0], parts[1]
    return None, None

def load_keys():
    keys = []
    if os.path.exists(KEYS_FILE):
        with open(KEYS_FILE, 'r', encoding='utf-8') as f:
            for line in f:
                if '|' in line:
                    parts = line.strip().split('|')
                    if len(parts) >= 3:
                        keys.append({
                            'login': parts[0].strip(),
                            'password': parts[1].strip(),
                            'expires': parts[2].strip()
                        })
    return keys

def check_key(login, password):
    keys = load_keys()
    for key in keys:
        if key['login'] == login and key['password'] == password:
            expires = datetime.datetime.strptime(key['expires'], '%Y-%m-%d')
            if datetime.datetime.now() <= expires:
                days_left = (expires - datetime.datetime.now()).days
                return True, f"✅ Вход выполнен! Осталось {days_left} дн."
            else:
                return False, "❌ Срок действия истёк!"
    return False, "❌ Неверный логин или пароль!"

def check_session():
    saved_login, saved_mac = load_session()
