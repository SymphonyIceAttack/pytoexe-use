import re
from datetime import datetime

def validate_date_format(date_str):
    """检查日期格式是否为yyyymmdd"""
    if not re.match(r'^\d{8}$', date_str):
        return False
    try:
        datetime.strptime(date_str, '%Y%m%d')
        return True
    except ValueError:
        return False

