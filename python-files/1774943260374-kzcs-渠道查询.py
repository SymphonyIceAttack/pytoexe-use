def search_channel():
    print("="*60)
    print("      渠道-客服-平台查询系统（网页版）      ")
    print("="*60)
    print("提示：输入渠道名称查询，输入'q'退出系统")
    print("="*60)
    while True:
        query = input("请输入要查询的渠道名称：").strip()
        if query.lower() == 'q':
            print("感谢使用，再见！")
            break
        if not query:
            print("⚠️  请输入有效的渠道名称！\n")
            continue
        
        # 精确匹配
        if query in channel_mapping:
            res = channel_mapping[query]
            print(f"✅ 精确匹配结果")
            print(f"📌 渠道名称：{query}")
            print(f"👨💼 负责客服：{res['客服']}")
            print(f"🏢 所属平台：{res['平台']}\n")
            continue
        
        # 模糊匹配
        fuzzy_res = []
        query_low = query.lower()
        for ch, info in channel_mapping.items():
            if query_low in ch.lower():
                fuzzy_res.append((ch, info))
        
        if fuzzy_res:
            print(f"⚠️  未找到精确匹配，共找到 {len(fuzzy_res)} 个相似渠道：\n")
            for i, (ch, info) in enumerate(fuzzy_res, 1):
                print(f"{i}. 渠道：{ch} | 客服：{info['客服']} | 平台：{info['平台']}")
            print()
        else:
            print(f"❌ 未找到渠道「{query}」的相关信息！\n")

# 启动查询系统
if __name__ == "__main__":
    search_channel()