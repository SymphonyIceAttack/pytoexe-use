# @title
def create_dossier():
    """Создание досье военнослужащего"""

    print("=" * 60)
    print("СОЗДАНИЕ ДОСЬЕ ВОЕННОСЛУЖАЩЕГО")
    print("=" * 60)

    dossier = {}

    # Общие сведения
    print("\n" + "=" * 60)
    print("ОБЩИЕ СВЕДЕНИЯ")
    print("=" * 60)

    dossier['ФИО'] = input("ФИО: ")
    dossier['Дата рождения'] = input("Дата рождения (ДД.ММ.ГГГГ): ")
    dossier['Место рождения'] = input("Место рождения: ")
    dossier['Гражданство'] = input("Гражданство: ")

    # Воинское звание
    print("\n" + "=" * 60)
    print("ВОИНСКОЕ ЗВАНИЕ")
    print("=" * 60)

    dossier['Звание'] = input("Звание: ")

    # Образование
    print("\n" + "=" * 60)
    print("ОБРАЗОВАНИЕ")
    print("=" * 60)

    dossier['Образование'] = input("Образование: ")
    dossier['Специальность'] = input("Специальность: ")
    dossier['Год окончания'] = input("Год окончания учебного заведения: ")

    # Опыт службы
    print("\n" + "=" * 60)
    print("ОПЫТ СЛУЖБЫ")
    print("=" * 60)

    dossier['Дата начала службы'] = input("Дата начала службы (ДД.ММ.ГГГГ): ")
    dossier['Подразделение'] = input("Подразделение: ")
    dossier['Должность'] = input("Должность: ")

    # Участие в боевых действиях
    print("\n" + "=" * 60)
    print("УЧАСТИЕ В БОЕВЫХ ДЕЙСТВИЯХ")
    print("=" * 60)
    print("Введите информацию о боевых действиях (для завершения введите 'готово'):")

    combat_actions = []
    count = 1
    while True:
        print(f"\nБоевое действие #{count}:")
        period = input("Период участия (например, '2015-2016' или 'ММ.ГГГГ - ММ.ГГГГ'): ")
        if period.lower() == 'готово':
            break
        place = input("Место действий: ")
        combat_actions.append((count, period, place))
        count += 1

    dossier['Боевые действия'] = combat_actions

    # Награды и поощрения
    print("\n" + "=" * 60)
    print("НАГРАДЫ И ПООЩРЕНИЯ")
    print("=" * 60)
    print("Введите информацию о наградах (для завершения введите 'готово'):")

    awards = []
    count = 1
    while True:
        print(f"\nНаграда #{count}:")
        date = input("Дата награждения (ДД.ММ.ГГГГ): ")
        if date.lower() == 'готово':
            break
        award = input("Вид награды: ")
        awards.append((count, date, award))
        count += 1

    dossier['Награды'] = awards

    # Дисциплинарные взыскания
    print("\n" + "=" * 60)
    print("ДИСЦИПЛИНАРНЫЕ ВЗЫСКАНИЯ")
    print("=" * 60)
    print("Введите информацию о взысканиях (для завершения введите 'готово'):")

    violations = []
    count = 1
    while True:
        print(f"\nВзыскание #{count}:")
        date = input("Дата нарушения (ДД.ММ.ГГГГ): ")
        if date.lower() == 'готово':
            break
        violation = input("Характер нарушения: ")
        violations.append((count, date, violation))
        count += 1

    dossier['Взыскания'] = violations

    # Медицинские сведения
    print("\n" + "=" * 60)
    print("МЕДИЦИНСКИЕ СВЕДЕНИЯ")
    print("=" * 60)

    dossier['Группа здоровья'] = input("Группа здоровья: ")
    dossier['Психологическое состояние'] = input("Психологическое состояние: ")

    # Дополнительные сведения
    print("\n" + "=" * 60)
    print("ДОПОЛНИТЕЛЬНЫЕ СВЕДЕНИЯ")
    print("=" * 60)

    driving = input("Умение водить автомобиль (да/нет): ").lower()
    dossier['Водит автомобиль'] = 'да' if driving == 'да' else 'нет'
    dossier['Иностранные языки'] = input("Владение иностранными языками: ")
    dossier['Водительские права'] = input("Наличие водительских прав категории: ")

    # Контактная информация
    print("\n" + "=" * 60)
    print("КОНТАКТНАЯ ИНФОРМАЦИЯ")
    print("=" * 60)

    dossier['Телефон'] = input("Телефон: ")
    dossier['Адрес'] = input("Адрес проживания: ")

    # Вывод досье
    print_dossier(dossier)

    # Сохранение в файл
    save_to_file(dossier)

    return dossier

