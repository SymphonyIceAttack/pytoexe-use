    def confiscate_property(self, amount_needed):
        """–ö–æ–Ω—Ñ–∏—Å–∫—É–µ—Ç –∏–º—É—â–µ—Å—Ç–≤–æ –∑–∞ –¥–æ–ª–≥–∏"""
        print(f"\nüîç –ò—â—É—Ç —á—Ç–æ –∑–∞–±—Ä–∞—Ç—å –Ω–∞ ${amount_needed:.2f}...")
        self.add_time(0.5, "–æ–±—ã—Å–∫")
        
        confiscated = 0.0
        
        if self.food_supply > 0:
            food_value = min(amount_needed // 10, self.food_supply) * 10
            if food_value > 0:
                self.food_supply -= int(food_value // 10)
                confiscated += food_value
                print(f"üì¶ –ó–∞–±—Ä–∞–ª–∏ {int(food_value//10)} –µ–¥—ã (${food_value:.2f})")
        
        items_to_confiscate = []
        
        if self.keyboard > 1:
            items_to_confiscate.append(("–∫–ª–∞–≤–∏–∞—Ç—É—Ä—É", 50 * (self.keyboard - 1), 
                                       lambda: setattr(self, 'keyboard', max(1, self.keyboard - 1))))
        
        if self.coffee > 1:
            items_to_confiscate.append(("–∫–æ—Ñ–µ", 30 * (self.coffee - 1),
                                       lambda: setattr(self, 'coffee', max(1, self.coffee - 1))))
        
        if self.monitor > 1:
            items_to_confiscate.append(("–º–æ–Ω–∏—Ç–æ—Ä", 100 * (self.monitor - 1),
                                       lambda: setattr(self, 'monitor', max(1, self.monitor - 1))))
        
        for item_name, item_data in self.furniture.items():
            if item_data["owned"]:
                items_to_confiscate.append((item_name, item_data["price"] * item_data["level"],
                                           lambda i=item_name: self.furniture[i].update({"owned": False, "level": 1})))
        
        items_to_confiscate.sort(key=lambda x: x[1])
        
        for item_name, value, confiscate_func in items_to_confiscate:
            if confiscated < amount_needed:
                confiscate_func()
