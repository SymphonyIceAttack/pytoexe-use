    def random_event(self):
        """–°–ª—É—á–∞–π–Ω–æ–µ –Ω–µ–≥–∞—Ç–∏–≤–Ω–æ–µ —Å–æ–±—ã—Ç–∏–µ —Å —É—á–µ—Ç–æ–º –º–µ–±–µ–ª–∏"""
        # –ë–∞–∑–æ–≤–∞—è –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —Å–æ–±—ã—Ç–∏—è - 25%
        base_probability = 0.25
        
        # –£–º–µ–Ω—å—à–µ–Ω–∏–µ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–∏ –æ—Ç –º–µ–±–µ–ª–∏ (0.5% –∑–∞ —É—Ä–æ–≤–µ–Ω—å –∫–∞–∂–¥–æ–≥–æ)
        reduction = 0.0
        if self.furniture["–ª–∞–º–ø–∞"]["owned"]:
            reduction += self.furniture["–ª–∞–º–ø–∞"]["level"] * 0.005
        if self.furniture["—Å—Ç—É–ª"]["owned"]:
            reduction += self.furniture["—Å—Ç—É–ª"]["level"] * 0.005
        if self.furniture["—Å—Ç–æ–ª"]["owned"]:
            reduction += self.furniture["—Å—Ç–æ–ª"]["level"] * 0.005
        
        final_probability = max(0.05, base_probability - reduction)  # –ú–∏–Ω–∏–º—É–º 5%
        
        if random.random() < final_probability:
            effect_text, money_loss, hunger_loss, energy_loss, time_cost = random.choice(self.negative_events)
            print(f"\nüì∞ {effect_text}")
            self.add_time(time_cost, "—Å–ª—É—á–∞–π–Ω–æ–µ —Å–æ–±—ã—Ç–∏–µ")
            
            if money_loss < 0:
                self.money = max(-1000.0, self.money + money_loss)
            
            if hunger_loss < 0:
                self.hunger = max(0.0, self.hunger + hunger_loss)
            
            if energy_loss < 0:
                self.energy = max(0.0, self.energy + energy_loss)
            
            return True
        return False
    
