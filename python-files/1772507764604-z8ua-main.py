class SimpleKabu(QWidget):
    def __init__(self):
        super().__init__()
        self.setWindowTitle("傻春擂台叼大版")
        self.resize(450, 280)
        self.driver = None
        self.running = Event()
        self.init_ui()

    def init_ui(self):
        # 简单界面布局
        layout = QVBoxLayout()
        
        # 1. 账号选择
        layout.addWidget(QLabel("选择账号："))
        self.acc_combo = QComboBox()
        self.acc_combo.addItems([acc.split("----")[0] for acc in ACCOUNTS])
        layout.addWidget(self.acc_combo)
        
        # 2. 服务器选择（仅支持1-10）
        layout.addWidget(QLabel("服务器号（1-10）："))
        self.server_input = QLineEdit()
        self.server_input.setPlaceholderText("只能输入1-10的数字")
        layout.addWidget(self.server_input)
        
        # 3. 模式选择
        self.mode_single = QRadioButton("单妖", checked=True)
        layout.addWidget(self.mode_single)
        
        # 4. 状态显示
        self.status_label = QLabel("状态：未登录")
        layout.addWidget(self.status_label)
        
        # 5. 按钮
        btn_layout = QHBoxLayout()
        self.login_btn = QPushButton("登录")
        self.start_btn = QPushButton("启动打擂")
        self.stop_btn = QPushButton("停止")
        self.start_btn.setEnabled(False)
        
        btn_layout.addWidget(self.login_btn)
        btn_layout.addWidget(self.start_btn)
        btn_layout.addWidget(self.stop_btn)
        layout.addLayout(btn_layout)
        
        self.setLayout(layout)
        
        # 绑定按钮事件
        self.login_btn.clicked.connect(self.login)
        self.start_btn.clicked.connect(self.start_fight)
        self.stop_btn.clicked.connect(self.stop_fight)

    def login(self):
        """登录游戏（校验服务器1-10）"""
        try:
            # 严格校验服务器号（仅1-10）
            server_num = self.server_input.text().strip()
            if not server_num.isdigit() or not (1 <= int(server_num) <= 10):
                self.status_label.setText("状态：服务器号只能是1-10的数字！")
                return
            
            # 获取选中的账号密码
            acc = ACCOUNTS[self.acc_combo.currentIndex()]
            _, pwd = acc.split("----")
            
            self.status_label.setText("状态：正在登录...")
            # 打开浏览器（防检测）
            opts = webdriver.ChromeOptions()
            opts.add_experimental_option("excludeSwitches", ["enable-automation"])
            self.driver = webdriver.Chrome(options=opts)
            
            # 访问游戏并登录
            self.driver.get(f"https://www.4399.com/flash/105229.htm?server={server_num}&key={pwd}")
            self.driver.switch_to.frame(self.driver.find_element(By.TAG_NAME, "iframe"))
            
            self.status_label.setText("状态：登录成功！可启动")
            self.login_btn.setEnabled(False)
            self.start_btn.setEnabled(True)
        except:
            self.status_label.setText("状态：登录失败！检查账号/Chrome浏览器")
            if self.driver:
                self.driver.quit()

    def auto_fight(self):
        """自动打擂台（移除所有截图相关代码）"""
        mode = "单妖" if self.mode_single.isChecked() else "多妖"
        count = 0
        
        while self.running.is_set():
            try:
                count += 1
                self.status_label.setText(f"状态：第{count}场{mode}战斗中")
                
                # 点击擂台
                self.driver.find_element(By.XPATH, "//*[contains(text(), '擂台')]").click()
                time.sleep(1)
                
                # 选择模式
                self.driver.find_element(By.XPATH, f"//*[contains(text(), '{mode}')]").click()
                time.sleep(1)
                
                # 开始战斗
                self.driver.find_element(By.XPATH, "//*[contains(text(), '开始战斗')]").click()
                time.sleep(30)  # 等待战斗完成（无需截图识别）
                
                # 点击返回
                self.driver.find_element(By.XPATH, "//*[contains(text(), '返回')]").click()
                time.sleep(random.uniform(2, 3))
                
            except:
                time.sleep(3)
                continue

    def start_fight(self):
        """启动自动战斗"""
        self.running.set()
        Thread(target=self.auto_fight, daemon=True).start()
        self.start_btn.setEnabled(False)
        self.stop_btn.setEnabled(True)

    def stop_fight(self):
        """停止战斗"""
        self.running.clear()
        if self.driver:
            self.driver.quit()
        self.status_label.setText("状态：已停止")
        self.login_btn.setEnabled(True)
        self.stop_btn.setEnabled(False)

