    def analyse_ca_trend(self):
        """Analyse des tendances du CA"""

        if len(self.crm.transactions_df) == 0:
            print("❌ Aucune donnée disponible")
            return

        # CA par mois
        self.crm.transactions_df['Mois'] = pd.to_datetime(self.crm.transactions_df['Date']).dt.to_period('M')
        ca_mensuel = self.crm.transactions_df.groupby('Mois')['CA Total'].sum()

        fig, axes = plt.subplots(2, 2, figsize=(15, 10))

        # Graphique 1: Évolution mensuelle
        ax = axes[0, 0]
        ca_mensuel.plot(kind='line', marker='o', ax=ax, color='#667eea', linewidth=2)
        ax.set_title('Évolution Mensuelle du CA', fontsize=14, fontweight='bold')
        ax.set_xlabel('Mois')
        ax.set_ylabel('CA (DHS)')
        ax.grid(True, alpha=0.3)

        # Graphique 2: Top 10 clients
        ax = axes[0, 1]
        top_clients = self.crm.transactions_df.groupby('Client_ID')['CA Total'].sum().nlargest(10)
        client_names = []
        for client_id in top_clients.index:
            client_name = self.crm.clients_df[self.crm.clients_df['ID'] == client_id]['Société'].values
            client_names.append(client_name[0] if len(client_name) > 0 else f'Client {client_id}')

        bars = ax.bar(range(len(top_clients)), top_clients.values, color=sns.color_palette('husl', 10))
        ax.set_title('Top 10 Clients', fontsize=14, fontweight='bold')
        ax.set_xlabel('Client')
        ax.set_ylabel('CA (DHS)')
        ax.set_xticks(range(len(top_clients)))
        ax.set_xticklabels(client_names, rotation=45, ha='right')

