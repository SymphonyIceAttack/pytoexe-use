class DataExporter:
    """
    Classe pour l'export et la sauvegarde des données
    """

    def __init__(self, crm):
        self.crm = crm

    def export_excel_complet(self, filename='snep_crm_export.xlsx'):
        """Exporte toutes les données vers Excel"""

        with pd.ExcelWriter(filename, engine='xlsxwriter') as writer:
            # Feuille Résumé
            resume_data = {
                'Métrique': ['CA Total', 'Nombre de transactions', 'Nombre de clients', 'Nombre de produits'],
                'Valeur': [
                    f"{self.crm.transactions_df['CA Total'].sum():,.0f} DHS" if len(self.crm.transactions_df) > 0 else '0 DHS',
                    str(len(self.crm.transactions_df)),
                    str(len(self.crm.clients_df)),
                    str(len(self.crm.produits_df))
                ]
            }
            pd.DataFrame(resume_data).to_excel(writer, sheet_name='Résumé', index=False)

            # Feuille Clients
            if len(self.crm.clients_df) > 0:
                self.crm.clients_df.to_excel(writer, sheet_name='Clients', index=False)

            # Feuille Produits
            if len(self.crm.produits_df) > 0:
                self.crm.produits_df.to_excel(writer, sheet_name='Produits', index=False)

            # Feuille Transactions
            if len(self.crm.transactions_df) > 0:
                self.crm.transactions_df.to_excel(writer, sheet_name='Transactions', index=False)

            # Statistiques
            stats = pd.DataFrame({
                'Catégorie': self.crm.transactions_df.groupby('Catégorie')['CA Total'].sum().index,
                'CA Total': self.crm.transactions_df.groupby('Catégorie')['CA Total'].sum().values
            }) if len(self.crm.transactions_df) > 0 else pd.DataFrame()

            if len(stats) > 0:
                stats.to_excel(writer, sheet_name='Statistiques', index=False)

            # Formatage Excel
            workbook = writer.book

            # Format pour les montants
            money_format = workbook.add_format({'num_format': '#,##0.00 DHS', 'bold': False})

            # Appliquer le format aux colonnes appropriées
            for sheet_name in writer.sheets:
                worksheet = writer.sheets[sheet_name]
                if 'CA' in sheet_name or 'Montant' in sheet_name or 'Prix' in sheet_name:
                    worksheet.set_column('B:B', 15, money_format)

        print(f"✅ Données exportées vers {filename}")
        return filename

    def export_csv(self, prefix='snep_crm'):
        """Exporte les données en CSV"""

        import os

        if not os.path.exists('exports'):
            os.makedirs('exports')

        timestamp = datetime.now().strftime('%Y%m%d_%H%M%S')

        if len(self.crm.clients_df) > 0:
            self.crm.clients_df.to_csv(f'exports/{prefix}_clients_{timestamp}.csv', index=False)

        if len(self.crm.produits_df) > 0:
            self.crm.produits_df.to_csv(f'exports/{prefix}_produits_{timestamp}.csv', index=False)

        if len(self.crm.transactions_df) > 0:
            self.crm.transactions_df.to_csv(f'exports/{prefix}_transactions_{timestamp}.csv', index=False)

        print(f"✅ Données exportées dans le dossier 'exports/'")

    def sauvegarder_etat(self, filename='snep_crm_backup.pkl'):
        """Sauvegarde l'état complet du CRM"""

        import pickle

        etat = {
            'clients': self.crm.clients_df,
            'contacts': self.crm.contacts_df,
            'produits': self.crm.produits_df,
            'transactions': self.crm.transactions_df,
            'opportunites': self.crm.opportunites_df,
            'taches': self.crm.taches_df,
            'timestamp': datetime.now()
        }

        with open(filename, 'wb') as f:
            pickle.dump(etat, f)

        print(f"✅ État sauvegardé dans {filename}")

    def charger_etat(self, filename='snep_crm_backup.pkl'):
        """Charge un état sauvegardé"""

        import pickle
        import os

        if not os.path.exists(filename):
            print(f"❌ Fichier {filename} non trouvé")
            return

        with open(filename, 'rb') as f:
            etat = pickle.load(f)

        self.crm.clients_df = etat['clients']
        self.crm.contacts_df = etat['contacts']
        self.crm.produits_df = etat['produits']
        self.crm.transactions_df = etat['transactions']
        self.crm.opportunites_df = etat['opportunites']
        self.crm.taches_df = etat['taches']

        print(f"✅ État chargé depuis {filename}")
        print(f"📅 Sauvegarde du {etat['timestamp'].strftime('%d/%m/%Y %H:%M')}")

