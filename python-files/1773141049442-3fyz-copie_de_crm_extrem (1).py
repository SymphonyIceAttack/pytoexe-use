    def ajouter_transaction(self, client_id, produit, quantite, prix_unitaire,
                             paiement='', echeance='', commercial='OUSSAMA RAMZI'):
        """Ajoute une nouvelle transaction"""

        id_transaction = len(self.transactions_df) + 1
        date = datetime.now().strftime('%Y-%m-%d')
        ca_total = quantite * prix_unitaire

        # Trouver la catégorie du produit
        produit_info = self.produits_df[self.produits_df['Nom'] == produit]
        if len(produit_info) > 0:
            categorie = produit_info.iloc[0]['Catégorie']
            unite = produit_info.iloc[0]['Unité']
        else:
            categorie = 'Autre'
            unite = 'Unité'

        # Déterminer l'alerte
        alerte = self._determiner_alerte(paiement)

        # Date de relance
        date_relance = ''
        if 'CHÈQUE' in paiement.upper() or 'CHEQUE' in paiement.upper():
            date_relance = (datetime.now() + timedelta(days=21)).strftime('%Y-%m-%d')

        nouvelle_transaction = pd.DataFrame([{
            'ID': id_transaction,
            'Date': date,
            'Client_ID': client_id,
            'Contact_ID': '',
            'Commercial': commercial,
            'Produit': produit,
            'Catégorie': categorie,
            'Unité': unite,
            'Quantité': quantite,
            'Prix Unitaire': prix_unitaire,
            'CA Total': ca_total,
            'Devise': 'DHS',
            'Paiement': paiement,
            'Échéance': echeance,
            'Statut': 'En cours',
            'Étape Pipeline': 'Prospection',
            'Probabilité': 50,
            'Date Clôture': '',
            'Notes': '',
            'Alerte': alerte,
            'Date Relance': date_relance,
            'Priorité': 'Moyenne'
        }])

        self.transactions_df = pd.concat([self.transactions_df, nouvelle_transaction], ignore_index=True)

        # Mettre à jour le CA du client
        client_idx = self.clients_df[self.clients_df['ID'] == client_id].index
        if len(client_idx) > 0:
            self.clients_df.loc[client_idx, 'CA Annuel'] += ca_total
            if self.clients_df.loc[client_idx, 'Statut'].values[0] == 'Prospect':
                self.clients_df.loc[client_idx, 'Statut'] = 'Client actif'

        return id_transaction

    def _determiner_alerte(self, paiement):
        """Détermine l'alerte en fonction du paiement"""
        paiement_upper = paiement.upper()

        if 'RETARD' in paiement_upper:
            return '🔴 URGENT'
        elif 'CHÈQUE' in paiement_upper or 'CHEQUE' in paiement_upper:
            return '🟡 À SURVEILLER'
        elif 'LITIGE' in paiement_upper:
            return '🔴 LITIGE'
        elif 'COMPTANT' in paiement_upper:
            return '🟢 OK'
        else:
            return '✓ SUIVI'

