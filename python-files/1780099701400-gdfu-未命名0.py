def add_new_poem():
    """添加新诗词(支持持久化)"""
    print("\n✍️ 添加新文章")
    print("(输入'q'返回主菜单)")
    
    title = input("请输入文章标题:").strip()
    if title.lower() == 'q':
        return
    if title in INVALID_VALUES:
        print("❌ 标题不能为无效值!")
        return
    
    author = input("请输入作者:").strip()
    if author.lower() == 'q':
        return
    if author in INVALID_VALUES:
        print("❌ 作者不能为无效值!")
        return
    
    dynasty = input("请输入朝代:").strip()
    if dynasty.lower() == 'q':
        return
    # 允许朝代为空值(INVALID_VALUES包含空字符串),此处取消无效值判断
    # 原代码此处的无效值判断会导致无法输入空朝代,与初始数据逻辑冲突
    
    print("请输入诗词内容(输入单独一行的'END'结束):")
    lines = []
    while True:
        line = input().strip()
        if line.lower() == 'end':
            break
        lines.append(line)
    
    if not lines:
        print("❌ 未输入内容,取消添加")
        return
    
    content = "\n".join(lines)
    
    # 确认添加
    print(f"\n📝 即将添加的诗词:")
    print(f"标题:<{title}>")
    print(f"作者:{author}")
    print(f"朝代:{dynasty}")
    print(f"内容:\n{content}")
    
    confirm = input("\n✅ 确认添加?(y/n): ").strip().lower()
    if confirm == 'y':
        new_poem = {
            "title": title,
            "author": author,
            "dynasty": dynasty,
            "content": content
        }
        poetry_data.append(new_poem)
        save_data(poetry_data)  # 持久化保存
        print("✅ 添加成功! 数据已保存")
    else:
        print("❌ 已取消添加")

def show_statistics():
    """显示统计信息"""
    print("\n📊 诗词库统计信息")
    print("-"*30)
    
    # 按朝代统计
    dynasty_count = {}
    for poem in poetry_data:
        dynasty = poem['dynasty']
        if dynasty not in INVALID_VALUES:
            dynasty_count[dynasty] = dynasty_count.get(dynasty, 0) + 1
    
    print("按朝代统计:")
    for dynasty, count in sorted(dynasty_count.items()):
        print(f"  {dynasty}代: {count}首")
    
    # 按作者统计
    author_count = {}
    for poem in poetry_data:
        author = poem['author']
        if author not in INVALID_VALUES:
            author_count[author] = author_count.get(author, 0) + 1
    
    # 找出作品最多的作者
    if author_count:
        top_author = max(author_count.items(), key=lambda x: x[1])
        print(f"\n🏆 作品最多的作者:{top_author[0]} ({top_author[1]}首)")
    
    # 诗词总数
    print(f"\n📖 诗词总数:{len(poetry_data)}首")
    
    # 诗句总数(按行数计算)
    total_lines = 0
    for poem in poetry_data:
        total_lines += len(poem['content'].split('\n'))
    print(f"📝 诗句总数:{total_lines}行")

