# ===================== 数据持久化函数 =====================
def load_data():
    """从JSON文件加载诗词数据,文件不存在则初始化并保存"""
    if os.path.exists(DATA_FILE):
        try:
            with open(DATA_FILE, 'r', encoding='utf-8') as f:
                data = json.load(f)
            # 过滤加载数据中的无效条目
            return [item for item in data if item.get('title') not in INVALID_VALUES]
        except (json.JSONDecodeError, FileNotFoundError):
            print("⚠️  数据文件损坏,使用初始数据重建...")
            save_data(initial_poetry_data)
            return initial_poetry_data
    else:
        # 首次运行,保存初始数据
        save_data(initial_poetry_data)
        return initial_poetry_data

def save_data(data):
    """将诗词数据保存到JSON文件(持久化)"""
    try:
        with open(DATA_FILE, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
    except Exception as e:
        print(f"❌ 保存数据失败: {e}")

