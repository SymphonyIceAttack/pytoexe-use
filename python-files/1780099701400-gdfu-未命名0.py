def copy_to_clipboard(poem):
    """复制诗词到剪贴板"""
    try:
        import pyperclip
        text = f"<{poem['title']}>\n{poem['author']} ({poem['dynasty']})\n\n{poem['content']}"
        pyperclip.copy(text)
        print("✅ 已复制到剪贴板!")
    except ImportError:
        print("⚠️ 需要安装pyperclip库才能使用复制功能.")
        print("💡 安装命令: pip install pyperclip")
        print("\n📝 以下是诗词文本,可手动复制:")
        print("-"*30)
        print(f"<{poem['title']}>\n{poem['author']} ({poem['dynasty']})\n\n{poem['content']}")
        print("-"*30)
    except Exception as e:
        print(f"❌ 复制失败: {e}")

