        types.InlineKeyboardButton("🛑 СТОП", callback_data="spam_stop"),
        types.InlineKeyboardButton("ℹ️ ИНФО", callback_data="info"),
        types.InlineKeyboardButton("📞 ВЛАДЕЛЕЦ", callback_data="owner")
