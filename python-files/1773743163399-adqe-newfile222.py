@bot.callback_query_handler(func=lambda call: True)
def callback(call):
    if call.from_user.id != ADMIN_ID:
        bot.answer_callback_query(call.id, "❌ Нет доступа")
        return
    
    if call.data == "off":
        shutdown_pc(10)
        bot.answer_callback_query(call.id, "🔴 Выключение через 10 сек")
    
    elif call.data == "restart":
        restart_pc(10)
        bot.answer_callback_query(call.id, "🔄 Перезагрузка через 10 сек")
    
    elif call.data == "cancel":
        cancel_shutdown()
        bot.answer_callback_query(call.id, "✅ Отменено")
    
    elif call.data == "spam_start":
        if Spamer.start(bot, call.message.chat.id):
            bot.answer_callback_query(call.id, "🤬 Спам запущен!")
        else:
            bot.answer_callback_query(call.id, "❌ Уже запущен")
    
    elif call.data == "spam_stop":
        if Spamer.stop():
            bot.answer_callback_query(call.id, "🛑 Спам остановлен")
    
    elif call.data == "info":
        bot.answer_callback_query(call.id, f"🖥️ {PC_NAME}\n🆔 {PC_ID}")
    
    elif call.data == "owner":
        bot.answer_callback_query(call.id, f"📞 {НОМЕР_ВЛАДЕЛЬЦА}")

