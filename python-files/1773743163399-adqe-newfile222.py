# ========== КЛАСС ДЛЯ СПАМА ==========
class Spamer:
    active = False
    thread = None
    
    @classmethod
    def start(cls, bot, chat_id):
        if cls.active:
            return False
        cls.active = True
        
        def spam():
            counter = 1
            while cls.active:
                insult = random.choice(УНИЖЕНИЯ_ЭПИЧНЫЕ + СУПЕР_ЖЕСТКИЕ)
                try:
                    bot.send_message(chat_id, f"🤬 **{counter}**\n{insult}")
                except:
                    pass
                counter += 1
                time.sleep(3)
        
        cls.thread = threading.Thread(target=spam, daemon=True)
        cls.thread.start()
        return True
    
    @classmethod
    def stop(cls):
        cls.active = False
        return True

