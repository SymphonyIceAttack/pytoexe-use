# 甜辣卤味全自动计算工具（按肉算：卤汤+调料+香料）
def calculate_all():
    import math

    # ========== 你的固定配方参数 ==========
    汤肉比例 = 0.6  # 汤:肉 = 1:0.6
    盐_min = 0.006
    盐_max = 0.008
    鸡精_min = 0.006
    鸡精_max = 0.008
    味精_min = 0.005
    味精_max = 0.006
    糖_min = 0.040
    糖_max = 0.045

    # 每斤汤的香料克数
    香料每斤 = {
        "肉桂": 0.73, "栀子": 0.36, "肉蔻": 0.55, "小茴香": 1.09, "香砂": 1.09,
        "草寇": 0.55, "良姜": 0.55, "白芷": 1.27, "五加皮": 0.36, "木香": 0.18,
        "香菜籽": 1.09, "草果": 0.55, "陈皮": 0.27, "毕拔": 0.27, "枳壳": 0.75,
        "千里香": 0.36, "黑胡椒": 0.36, "甘草片": 0.82, "白胡椒": 0.27, "白蔻": 0.91,
        "丁香": 0.27, "香叶": 0.36, "干姜": 0.91, "八角": 1.45, "山奈": 0.36, "槟榔": 0.18
    }

    # ========== 输入肉斤数 ==========
    while True:
        try:
            肉斤 = float(input("请输入卤肉斤数："))
            break
        except:
            print("请输入有效数字！")

    # ========== 1. 算卤汤 ==========
    卤汤斤 = 肉斤 / 汤肉比例
    卤汤克 = 卤汤斤 * 500
    总重克 = 卤汤克 + (肉斤 * 500)

    # ========== 2. 算调料 ==========
    盐 = (总重克 * 盐_min, 总重克 * 盐_max)
    鸡精 = (总重克 * 鸡精_min, 总重克 * 鸡精_max)
    味精 = (总重克 * 味精_min, 总重克 * 味精_max)
    糖 = (总重克 * 糖_min, 总重克 * 糖_max)

    # ========== 3. 辣椒、花椒、香精 ==========
    辣椒 = 卤汤斤 * 10.2
    花椒麻椒 = 卤汤斤 * 3.27
    鸭肉香精 = 卤汤斤 * 1.1
    麦芽糖 = 卤汤斤 * 4.7

    # ========== 4. 算香料 ==========
    香料总量 = sum(香料每斤.values()) * 卤汤斤

    # ========== 输出结果 ==========
    print("\n" + "=" * 55)
    print(f"卤肉斤数：{肉斤} 斤")
    print(f"需要卤汤：{卤汤斤:.1f} 斤")
    print(f"计算总重：{总重克 / 1000:.2f} kg")
    print("=" * 55)

    print("\n【调料用量（克）】")
    print(f"盐：{盐[0]:.1f} ~ {盐[1]:.1f}g  推荐：{(sum(盐)/2):.1f}g")
    print(f"鸡精：{鸡精[0]:.1f} ~ {鸡精[1]:.1f}g  推荐：{(sum(鸡精)/2):.1f}g")
    print(f"味精：{味精[0]:.1f} ~ {味精[1]:.1f}g  推荐：{(sum(味精)/2):.1f}g")
    print(f"糖：{糖[0]:.1f} ~ {糖[1]:.1f}g  推荐：{(sum(糖)/2):.1f}g")

    print("\n【辣椒/花椒/增香】")
    print(f"辣椒：{辣椒:.0f}g")
    print(f"花椒+麻椒：{花椒麻椒:.0f}g")
    print(f"鸭肉香精：{鸭肉香精:.0f}g")
    print(f"麦芽糖：{麦芽糖:.0f}g")

    print("\n【香料包（按肉自动计算）】")
    for name, g in 香料每斤.items():
        print(f"{name}：{g * 卤汤斤:.2f}g")

    print(f"\n香料总重量：{香料总量:.1f}g")
    print("=" * 55)
    input("\n计算完成！按回车退出...")

if __name__ == "__main__":
    calculate_all()