import time

def print_slow(text, delay=0.03):
    """Печатает текст с небольшой задержкой для эффекта презентации"""
    for char in text:
        print(char, end='', flush=True)
        time.sleep(delay)
    print()

def wait_for_enter():
    """Ожидает нажатия Enter для перехода к следующему слайду"""
    input("\nНажмите Enter, чтобы продолжить...")

def clear_screen():
    """Очищает экран (для разных ОС)"""
    import os
    os.system('cls' if os.name == 'nt' else 'clear')

def show_slide(title, content_lines):
    """Показывает один слайд презентации"""
    clear_screen()
    print("=" * 60)
    print(f"   {title}")
    print("=" * 60)
    print()
    for line in content_lines:
        print(f"• {line}")
    print()
    wait_for_enter()

def run_test():
    """Запускает тест из 6 вопросов и выводит результат"""
    clear_screen()
    print("=" * 60)
    print("   ТЕСТ: Что вы запомнили о народах России?")
    print("=" * 60)
    print()
    print("Отвечайте на вопросы, вводя номер правильного ответа (1, 2 или 3).\n")
    
    questions = [
        {
            "text": "Какой народ самый многочисленный в России?",
            "options": ["1. Татары", "2. Русские", "3. Башкиры"],
            "correct": 2
        },
        {
            "text": "Где традиционно живут чукчи?",
            "options": ["1. На берегах Волги", "2. На Чукотке и Камчатке", "3. В Сибири, около Байкала"],
            "correct": 2
        },
        {
            "text": "Какая из этих республик является родиной татарского народа?",
            "options": ["1. Башкортостан", "2. Чечня", "3. Татарстан"],
            "correct": 3
        },
        {
            "text": "Какой народ славится искусством горлового пения и живёт в горах Алтая?",
            "options": ["1. Алтайцы", "2. Ненцы", "3. Чуваши"],
            "correct": 1
        },
        {
            "text": "Где в основном проживают башкиры?",
            "options": ["1. В республике Башкортостан", "2. В Дагестане", "3. В Карелии"],
            "correct": 1
        },
        {
            "text": "Какой из этих народов коренной для Кавказа?",
            "options": ["1. Чукчи", "2. Чеченцы", "3. Якуты"],
            "correct": 2
        }
    ]
    
    score = 0
    for i, q in enumerate(questions, 1):
        print(f"Вопрос {i}: {q['text']}")
        for opt in q['options']:
            print(f"   {opt}")
        answer = input("\nВаш ответ (номер): ")
        # Проверяем, что введено число и оно соответствует одному из вариантов
        try:
            ans_num = int(answer)
            if ans_num == q['correct']:
                print("✅ Верно!\n")
                score += 1
            else:
                correct_opt = q['options'][q['correct']-1]
                print(f"❌ Неверно. Правильный ответ: {correct_opt}\n")
        except ValueError:
            print("❌ Нужно ввести номер варианта! Ответ не засчитан.\n")
        time.sleep(1)
    
    # Подведение итогов
    clear_screen()
    print("=" * 60)
    print("   РЕЗУЛЬТАТ ТЕСТА")
    print("=" * 60)
    print(f"\nВы набрали {score} баллов из {len(questions)}.")
    if score == len(questions):
        print("🎉 Отлично! Вы настоящий знаток народов России!")
    elif score >= len(questions) // 2:
        print("👍 Хорошо! Повторите материал, и будет ещё лучше!")
    else:
        print("📚 Неплохо! Запустите презентацию ещё раз и запомните интересные факты.")
    print("\nСпасибо за внимание! Дружба народов – наша общая сила!")

