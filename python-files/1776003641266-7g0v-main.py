import time
import sys

def main():
    print("\n" * 3)
    print("=" * 50)
    print("         УДАЛЕНИЕ ОПЕРАЦИОННОЙ СИСТЕМЫ WINDOWS")
    print("=" * 50)
    print("         Процесс необратим. Пожалуйста, не выключайте ПК.\n")

    # Обратный отсчет от 10 до 0
    for i in range(10, -1, -1):
        # Спецэффект: печатаем в одной строке
        print(f"   >> Осталось секунд: {i:2}   ", end="\r")
        time.sleep(1)

    print("\n\n" + "=" * 50)
    print("                     ШУТКА! :)")
    print("          Ничего не удалено. Можете выдохнуть.")
    print("=" * 50 + "\n")

    input("Нажмите Enter, чтобы закрыть окно...")

if __name__ == "__main__":
    main()