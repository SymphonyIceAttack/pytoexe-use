def main():
    print("=" * 30)
    print("       КАЛЬКУЛЯТОР (S * h * p)")
    print("=" * 30)
    
    # Константа
    p = 0.0088
    
    try:
        # Ввод значений с клавиатуры
        S = float(input("Введите значение S: "))
        h = float(input("Введите значение h: "))
        
        # Вычисление
        result = S * h * p
        
        # Вывод итога
        print("-" * 30)
        print(f"Результат (S * h * p) = {result}")
        print("=" * 30)
        
    except ValueError:
        print("Ошибка! Пожалуйста, вводите только числа.")
        
    # Эта строка нужна, чтобы окно exe-файла не закрывалось мгновенно после вычисления
    input("\nНажмите Enter для выхода...")

if __name__ == "__main__":
    main()