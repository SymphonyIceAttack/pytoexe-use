import tkinter as tk
# Создаем главное окно
window = tk.Tk()
window.title("Числомания")  # Устанавливаем заголовок окна
window.geometry("300x200")       # Устанавливаем размеры окна (ширина x высота)

# Добавляем текст в окно
label = tk.Label(window, text="Числомания,игра насчет ")
label.pack(pady=80)  # Размещаем текст с отступом сверху и снизу

# Добавляем кнопку
button = tk.Button(window, text="играть ", command=window.destroy)
button.pack()
label.pack(pady=60)  # Размещаем текст  сверху  в углу

# Добавляем кнопку
button = tk.Button(window, text="выйти ", command=window.destroy)
button.pack()
# Запускаем главный цикл окна
window.mainloop()

