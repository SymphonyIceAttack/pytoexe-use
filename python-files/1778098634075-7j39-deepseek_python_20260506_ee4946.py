class DiaryApp:
    def __init__(self, root):
        self.root = root
        self.root.title("Дневник Ткача Генова")
        self.root.geometry("700x550")
        self.root.configure(bg="#2e2e2e")

        self.current_page = 0

        # Заголовок страницы (дата)
        self.title_label = tk.Label(
            root,
            text="",
            font=("Courier New", 18, "bold"),
            fg="#e0c27b",
            bg="#2e2e2e",
            anchor="w",
            justify="left"
        )
        self.title_label.pack(pady=(20, 5), padx=30, fill="x")

        # Текст страницы с прокруткой
        self.text_area = scrolledtext.ScrolledText(
            root,
            wrap=tk.WORD,
            font=("Courier New", 12),
            bg="#1e1e1e",
            fg="#d4d4d4",
            insertbackground="white",
            borderwidth=0,
            highlightthickness=0,
            padx=15,
            pady=15
        )
        self.text_area.pack(pady=5, padx=30, fill="both", expand=True)
        self.text_area.config(state="disabled")  # только для чтения

        # Панель с кнопками
        button_frame = tk.Frame(root, bg="#2e2e2e")
        button_frame.pack(pady=10)

        self.prev_button = tk.Button(
            button_frame,
            text="◀ Предыдущая",
            font=("Arial", 11),
            bg="#444444",
            fg="#ffffff",
            activebackground="#666666",
            activeforeground="#ffffff",
            relief="flat",
            padx=15,
            pady=5,
            command=self.prev_page
        )
        self.prev_button.pack(side="left", padx=15)

        self.next_button = tk.Button(
            button_frame,
            text="Следующая ▶",
            font=("Arial", 11),
            bg="#444444",
            fg="#ffffff",
            activebackground="#666666",
            activeforeground="#ffffff",
            relief="flat",
            padx=15,
            pady=5,
            command=self.next_page
        )
        self.next_button.pack(side="left", padx=15)

        # Счётчик страниц
        self.page_counter = tk.Label(
            button_frame,
            text="",
            font=("Arial", 10),
            fg="#aaaaaa",
            bg="#2e2e2e"
        )
        self.page_counter.pack(side="left", padx=20)

        self.update_page()

    def update_page(self):
        page = pages[self.current_page]
        self.title_label.config(text=page["title"])
        self.text_area.config(state="normal")
        self.text_area.delete("1.0", "end")
        self.text_area.insert("1.0", page["text"])
        self.text_area.config(state="disabled")
        self.page_counter.config(
            text=f"{self.current_page + 1} / {len(pages)}"
        )

    def next_page(self):
        if self.current_page < len(pages) - 1:
            self.current_page += 1
            self.update_page()

    def prev_page(self):
        if self.current_page > 0:
            self.current_page -= 1
            self.update_page()

if __name__ == "__main__":
    root = tk.Tk()
    app = DiaryApp(root)
    root.mainloop()