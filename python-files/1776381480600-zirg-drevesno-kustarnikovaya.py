import tkinter as tk
from tkinter import ttk, font

class PlantGuideApp:
    def __init__(self, root):
        self.root = root
        self.root.title("Флора Беловежской пущи — Справочник")
        self.root.geometry("1050x750")
        
        # Настройка стилей
        self.default_font = font.nametofont("TkDefaultFont")
        self.default_font.configure(size=11)
        
        # ===== ВЕРХНЯЯ ПАНЕЛЬ: ВЫБОР КАТЕГОРИИ =====
        top_frame = ttk.Frame(root)
        top_frame.pack(fill=tk.X, padx=10, pady=(10, 5))
        
        ttk.Label(top_frame, text="Категория растений:", font=("Arial", 11, "bold")).pack(side=tk.LEFT, padx=(0, 10))
        
        self.category_var = tk.StringVar()
        self.category_combo = ttk.Combobox(top_frame, 
                                           textvariable=self.category_var,
                                           font=("Arial", 11),
                                           state="readonly",
                                           width=40)
        self.category_combo['values'] = ('🌳 Древесно-кустарниковая растительность (32 вида)', 
                                         '🌿 Травянистая растительность (30 видов)')
        self.category_combo.current(0)  # По умолчанию выбрана первая категория
        self.category_combo.bind('<<ComboboxSelected>>', self.on_category_change)
        self.category_combo.pack(side=tk.LEFT)
        
        # ===== ОСНОВНОЙ КОНТЕЙНЕР =====
        main_paned = ttk.PanedWindow(root, orient=tk.HORIZONTAL)
        main_paned.pack(fill=tk.BOTH, expand=True, padx=10, pady=(5, 10))
        
        # --- Левая панель: Список растений ---
        left_frame = ttk.Frame(main_paned, width=320)
        main_paned.add(left_frame, weight=1)
        
        self.list_title = ttk.Label(left_frame, text="Список видов:", font=("Arial", 12, "bold"))
        self.list_title.pack(anchor="w", pady=(0, 5))
        
        # Контейнер для списка с прокруткой
        list_frame = ttk.Frame(left_frame)
        list_frame.pack(fill=tk.BOTH, expand=True)
        
        scrollbar = ttk.Scrollbar(list_frame)
        scrollbar.pack(side=tk.RIGHT, fill=tk.Y)
        
        self.listbox = tk.Listbox(list_frame, 
                                  yscrollcommand=scrollbar.set,
                                  font=("Arial", 10),
                                  selectbackground="#4a6984",
                                  selectforeground="white",
                                  borderwidth=2,
                                  relief="sunken")
        self.listbox.pack(side=tk.LEFT, fill=tk.BOTH, expand=True)
        scrollbar.config(command=self.listbox.yview)
        
        self.listbox.bind('<<ListboxSelect>>', self.on_select)
        
        # --- Правая панель: Описание ---
        right_frame = ttk.Frame(main_paned)
        main_paned.add(right_frame, weight=2)
        
        # Заголовок
        self.title_label = ttk.Label(right_frame, text="Выберите растение из списка", 
                                     font=("Arial", 14, "bold"), anchor="center")
        self.title_label.pack(pady=(0, 10))
        
        # Текстовое поле с описанием
        text_frame = ttk.Frame(right_frame)
        text_frame.pack(fill=tk.BOTH, expand=True)
        
        text_scroll = ttk.Scrollbar(text_frame)
        text_scroll.pack(side=tk.RIGHT, fill=tk.Y)
        
        self.desc_text = tk.Text(text_frame, 
                                 wrap=tk.WORD, 
                                 yscrollcommand=text_scroll.set,
                                 font=("Arial", 11),
                                 padx=15,
                                 pady=15,
                                 borderwidth=1,
                                 relief="solid")
        self.desc_text.pack(side=tk.LEFT, fill=tk.BOTH, expand=True)
        text_scroll.config(command=self.desc_text.yview)
        
        # Теги для форматирования текста
        self.desc_text.tag_configure("latin", font=("Arial", 11, "italic"))
        self.desc_text.tag_configure("bold", font=("Arial", 11, "bold"))
        self.desc_text.tag_configure("title", font=("Arial", 12, "bold"), foreground="#2c3e50")
        self.desc_text.tag_configure("category_tag", font=("Arial", 10, "italic"), foreground="#7f8c8d")
        
        # Загрузка данных и инициализация
        self.all_plants = {
            "woody": WOODY_PLANTS,      # древесно-кустарниковые
            "herbaceous": HERBACEOUS_PLANTS  # травянистые
        }
        self.current_category = "woody"
        self.update_listbox()
        
    def on_category_change(self, event=None):
        """Обработчик смены категории."""
        selected = self.category_var.get()
        if "Древесно" in selected:
            self.current_category = "woody"
            self.list_title.config(text="Древесно-кустарниковая растительность (32 вида):")
        else:
            self.current_category = "herbaceous"
            self.list_title.config(text="Травянистая растительность (30 видов):")
        
        self.update_listbox()
        self.clear_description()
    
    def update_listbox(self):
        """Обновляет содержимое Listbox в соответствии с выбранной категорией."""
        self.listbox.delete(0, tk.END)
        self.plants_data = {}
        self.names = []
        
        plants_list = self.all_plants[self.current_category]
        
        for plant in plants_list:
            name = plant['name']
            latin = plant['latin']
            desc = plant['desc']
            category = plant.get('category', '')
            
            display_name = f"{name} ({latin})"
            self.plants_data[display_name] = {
                "name": name,
                "latin": latin,
                "desc": desc,
                "category": category
            }
            self.names.append(display_name)
            self.listbox.insert(tk.END, display_name)
    
    def clear_description(self):
        """Очищает область описания."""
        self.title_label.config(text="Выберите растение из списка")
        self.desc_text.config(state=tk.NORMAL)
        self.desc_text.delete("1.0", tk.END)
        self.desc_text.config(state=tk.DISABLED)
    
    def on_select(self, event):
        """Обработчик выбора элемента в списке."""
        selection = self.listbox.curselection()
        if not selection:
            return
        
        index = selection[0]
        display_name = self.names[index]
        data = self.plants_data[display_name]
        
        # Обновляем заголовок
        self.title_label.config(text=f"{data['name']} — {data['latin']}")
        
        # Очищаем и заполняем текстовое поле
        self.desc_text.config(state=tk.NORMAL)
        self.desc_text.delete("1.0", tk.END)
        
        # Вставляем дублированное название в начале описания
        self.desc_text.insert(tk.END, f"{data['name']}\n", "title")
        self.desc_text.insert(tk.END, f"{data['latin']}\n", "latin")
        if data['category']:
            self.desc_text.insert(tk.END, f"{data['category']}\n", "category_tag")
        self.desc_text.insert(tk.END, "\n" + "─" * 50 + "\n\n")
        
        # Основное описание
        self.desc_text.insert(tk.END, data['desc'])
        
        self.desc_text.config(state=tk.DISABLED)


